package br.org.serratec.biblioteca.dtos;

public class Geolocation {
	private String lat;
	private String lon;

	
	
	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getlong() {
		return lon;
	}

	public void setLong(String lon) {
		this.lon = lon;
	}
}
